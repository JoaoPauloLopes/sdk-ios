import Foundation
import UIKit

class InstallmentTableViewCell : UITableViewCell {
    @IBOutlet private weak var installmentsLabel : UILabel!
    
    override init(style: UITableViewCellStyle, reuseIdentifier: String!) {
        super.init(style: style, reuseIdentifier: reuseIdentifier)
    }
    
    override func awakeFromNib() {
        super.awakeFromNib()
        // Initialization code
    }
    
    required init(coder aDecoder: NSCoder) {
        super.init(coder: aDecoder)
    }
    
    func fillWithPayerCost(payerCost : PayerCost, amount: Double) {
        installmentsLabel.text = payerCost.recommendedMessage
    }
}
